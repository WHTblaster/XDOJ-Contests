#include <stdio.h>
int main() {
	int S,T;
	scanf("%d", &S);
	if (S <= 3500) {
		T = S;
	}
	else if (S <= 5000) {
		T = 3500 + (S - 3500) * 97 / 100;
	}
	else if (S <= 8000) {
		T = 3500 + 1455 + (S - 5000) * 90 / 100;
	}
	else if (S <= 12500) {
		T = 3500 + 1455 + 2700 + (S - 8000) * 80 / 100;
	}
	else if (S <= 38500) {
		T = 3500 + 1455 + 2700 + 3600 + (S - 12500) * 75 / 100;
	}
	else {
		T = 3500 + 1455 + 2700 + 3600 + 19500 + (S - 38500) * 70 / 100;
	}
	printf("%d", T);
	return 0;
}